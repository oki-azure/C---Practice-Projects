/***************************************************************************************************************************************
Write a program to create the following pattern as the output,
12
123
1234
12345
***************************************************************************************************************************************/

#include <iostream>

int main() {
    for (size_t i{1}, j{1}, k{1}; i < 15; ++i) {
        while(j <= i+1) {
            std::cout << k;

            ++k; ++j;
        }

        std::cout << std::endl;

        k = 1; j = 1;
    }
}